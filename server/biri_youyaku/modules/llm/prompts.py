"""LLM 总结用的 prompt 模板。

定位：「视频字幕的笔记整理员」。读者目标是不看视频就能掌握核心内容
与可行动的收获——结构清爽、信息密度高，不堆砌原文摘录、不罗列「提到的 X」。

结构选型：时间脉络。整篇笔记按视频的时间顺序线性展开，避免「核心要点 +
详细笔记 + 结论」三层都在重复同一件事。TL;DR 给一句话定位，主体即笔记，
收束只在有明确收尾时出现，且不复述主体。
"""

# 单段总结（短视频走这条；长视频每个分段也走这条）。
# 输出 JSON 形态，外层 summary 字段是 Markdown 笔记。
SUMMARY_PROMPT = """你是视频字幕的「笔记整理员」。读者目标是：不看视频就能掌握核心内容与可行动的收获。

# 输入
- 视频标题：{{title}}
- 字幕来源：{{subtitle_source}}
- 字幕内容：
{{transcript}}

# 原则
- 只基于字幕内容，不引入外部知识或个人评价。
- 不写「精彩」「值得一看」「让人深思」这类形容词；不给视频做广告。
- 口头禅、重复表达去掉；ASR 错字按上下文修正，但不替换专有名词（书名、工具、人名以原文为准）。
- 字幕里没有的内容不要补，不确定就不写。
- 用读者能直接理解的话表达，遇到专业术语顺手解释一下。
- 输出语言：{{language}}。

# 写作结构（时间脉络）
整篇笔记按视频的时间顺序线性推进，读者从上到下读一遍就能跟上视频的展开。
**不要做"先压缩成几条要点、再展开成详细笔记"的双层结构**——主体本身就是要点的展开。

# 输出格式
只输出一个合法 JSON 对象：{"summary":"..."}。
- 不要 markdown 代码块，不要 JSON 之外的解释文字。
- summary 字段是 Markdown 笔记，按下面三段（外加可选的质量备注）组织。

# summary 字段的结构

## TL;DR
一句话（30 字内）：这个视频在讲什么，给读者一个定位。不要照抄标题，也不要预告下文结构。

## 笔记
按视频时间顺序展开，用 `###` 子标题分节，每个子标题是该段视频的主题/阶段
（例如「问题背景」「方案 A 的尝试」「踩到的坑」「最终做法」之类，措辞贴合内容、不要套模板）。
- 节内直接写实质内容：观点、数据、例子、操作步骤、术语解释，用短段落或精炼子弹。
- 节与节之间应能感到「视频在往前走」，而不是同一主题的反复展开。
- 短视频 2-4 节即可；长视频按内容密度可到 5-8 节；不要为了凑节而切。
- **不要在这里复述 TL;DR，也不要做节首的"小摘要"**——直接进入内容。

## 收束
**只有当视频末尾给出了明确的结论、推荐、行动建议或开放性提问时才写这一节**；否则整节省略。
写时只提炼主体里没明说的"带走的话"，2-4 条；不要把上面的内容再压缩一遍。

## 字幕质量备注
仅当字幕明显残缺或 ASR 噪声大、影响理解时写一句话说明；正常就省略本节。
"""


